CREATE SCHEMA IF NOT EXISTS SCHEMA1;
CREATE SCHEMA IF NOT EXISTS SCHEMA2;

CREATE TABLE IF NOT EXISTS SCHEMA1.users (
  user_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
  username STRING NOT NULL,
  created_at TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (user_id)
);

CREATE TABLE IF NOT EXISTS SCHEMA1.accounts (
  account_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
  user_id NUMBER NOT NULL,
  balance NUMBER(10,2) DEFAULT 0,
  PRIMARY KEY (account_id)
);

CREATE TABLE IF NOT EXISTS SCHEMA2.orders (
  order_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
  user_id NUMBER NOT NULL,
  order_total NUMBER(10,2),
  created_at TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
  PRIMARY KEY (order_id)
);

CREATE TABLE IF NOT EXISTS SCHEMA2.products (
  product_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
  name STRING NOT NULL,
  price NUMBER(10,2),
  PRIMARY KEY (product_id)
);
